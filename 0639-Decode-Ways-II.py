class Solution(object):
    def numDecodings(self, s):
        p = 10 ** 9 + 7
        
        preComputed = {'1': 1, '2': 1, '3': 1, '4': 1, '5': 1, '6': 1, '7': 1, '8': 1, '9': 1, '*': 9,
                       '10': 1, '11': 1, '12': 1, '13': 1, '14': 1, '15': 1, '16': 1, '17': 1, '18': 1,
                       '19': 1, '20': 1, '21': 1, '22': 1, '23': 1, '24': 1, '25': 1, '26': 1, '*0': 2,
                       '*1': 2, '*2': 2, '*3': 2, '*4': 2, '*5': 2, '*6': 2, '*7': 1, '*8': 1, '*9': 1,
                       '1*': 9, '2*': 6, '**': 15}
        
        n = len(s)
        val1, val2 = preComputed.get(s[-1], 0), 1
        
        for i in range(n-2, -1, -1):
            val1, val2 = preComputed.get(s[i:i+1], 0) * val1 + preComputed.get(s[i:i+2], 0) * val2, val1
        
        return (val1 % p)